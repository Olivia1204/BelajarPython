a = int(input("Masukkan angka 1 : "))
b = int(input("Masukkan angka 2 : "))
operator_lebih_besar = a > b
operator_lebih_kecil = a < b
operator_sama_dengan = a == b
operator_tidaksama_dengan = a != b


print( a, ">", b, "adalah", operator_lebih_besar )
print( a, "<", b , "adalah", operator_lebih_kecil)
print( a, "==", b, "adalah", operator_sama_dengan)
print( a, "!=", b, "adalah", operator_tidaksama_dengan)
